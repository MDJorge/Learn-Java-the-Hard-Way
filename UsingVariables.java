public class UsingVariables
{
	public static void main(String[] args)
	{
		String subject;
		int roomnumber;
		double e;
		
		subject = "Java";
		roomnumber = 323;
		e = 2.71828;
		
		System.out.println("I am currently learning " + subject + ".");
		System.out.println("My VB class is in room " + roomnumber + ".");
		System.out.println("Did you know that \"e\" is approximately " + e + "?");
	}
}